from __future__ import annotations

import json
from typing import Any


SYSTEM_PROMPT = """Ты автономный инженерный агент.

Правила:
- Работай только в рамках задачи из таск-трекера и рабочего пространства.
- Используй инструменты для чтения, изменения файлов, команд, проверок, git-диагностики и работы с задачами через MCP.
- Не предполагай workflow и допустимые статусы задачи: сначала читай их через MCP tools.
- Если нужно перевести задачу в другой статус или оставить комментарий, делай это через MCP tools, а не только текстом в финальном ответе.
- Если для выполнения достаточно текстового ответа, не создавай файлы и не вызывай git.
- Не выполняй несвязанные рефакторинги.
- Если информации недостаточно, явно напиши это в финальном ответе.
- Перед завершением кратко опиши, что сделано и какие проверки выполнены.
- Не обещай действий вне доступных инструментов.
"""


def build_task_prompt(task: dict[str, Any]) -> str:
    payload = {
        "id": task.get("id"),
        "type": task.get("type"),
        "status": task.get("status"),
        "title": task.get("title"),
        "author_email": task.get("author_email"),
        "assignee_email": task.get("assignee_email"),
        "description": task.get("description"),
        "links": task.get("links") or [],
        "meta": task.get("meta") or {},
    }
    return (
        "Выполни задачу из таск-трекера. "
        "Сначала при необходимости дочитай workflow, комментарии и актуальное состояние задачи через MCP tools. "
        "Если по ходу работы нужно обновить статус или добавить комментарий, используй MCP tools явно. "
        "Данные задачи переданы как JSON:\n"
        f"{json.dumps(payload, ensure_ascii=False, indent=2)}"
    )
